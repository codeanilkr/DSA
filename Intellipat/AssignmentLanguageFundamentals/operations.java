package AssignmentLanguageFundamentals;

public class operations {
    public static void main(String[] args){
        int a = 5;
        int b = 3;
        int c = 7;
        System.out.println("I.  a + b = " + (a + b));
        System.out.println("II. a - b = " + (a - b));
        System.out.println("III. a * b = " + (a * b));
        System.out.println("IV.  a / b = " + (a / b));
        System.out.println("V.   a == b is " + (a == b));
        System.out.println("VI.  a != b is " + (a != b));
        System.out.println("VII. a > b is " + (a > b));
        System.out.println("VIII.a < b is " + (a < b));
        System.out.println("IX.  a & b = " + (a & b));
        System.out.println("X.   a ^ b = " + (a ^ b));
        System.out.println("XI.  a | b = " + (a | b));
        System.out.println("XII. (a > b) && (b > c) is " + ((a > b) && (b > c)));
        System.out.println("XIII.(a > b) && (b < c) is " + ((a > b) && (b < c)));
        System.out.println("XIV. (5 > 3) || (6 > 3) is " + ((5 > 3) || (6 > 3)));
        System.out.println("XV.  (5 > 3) || (6 < 3) is " + ((5 > 3) || (6 < 3)));
    }

    }
    

